# -------------------------------------------------------------------
# "Predecessor and succesor" program
# -------------------------------------------------------------------
# It takes one integer number and then prints its predecessor and succesor.
# -------------------------------------------------------------------

print("Ingrese un número entero: ")
num = int(input())

pre = num - 1
suc = num + 1

print("El antecesor de este número es: ", pre)
print("Y su sucesor: ", suc)